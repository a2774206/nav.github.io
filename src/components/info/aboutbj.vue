                    <h1>气候环境</h1>

                    <div class="gybj_cont">北京的气候为典型的暖温带半湿润大陆性季风气候，夏季高温多雨，冬季寒冷干燥，春、秋短促。2007年为例，全年平均气温14.0℃（北京市气象局）。1月-7至-4℃，7月25至26℃。极端最低-27.4℃，极端最高42℃以上。全年无霜期180至200天，西部山区较短。2007年平均降雨量483.9毫米，为华北地区降雨最多的地区之一。降水季节分配很不均匀，全年降水的80%集中在夏季6、7、8三个月，7、8月有大雨。</div>

                    <h1>太阳辐射</h1>

                    <div class="gybj_cont">北京太阳辐射量全年平均为112-136千卡/厘米。两个高值区分别分布在延庆盆地及密云县西北部至怀柔东部一带，年辐射量均在 135千卡/厘米以上；低值区位于房山区的霞云岭附近，年辐射量为112千卡/厘米。北京年平均日照时数在2000-2800小时之间。大部分地区在2600小时左右。年日照分布与太阳辐射的分布相一致，最大值在延庆县和古北口，为2800小时以上，最小值分布在霞云岭，日照为2063小时。全年日照时数以春季最多，月日照在230-290小时；夏季正当雨季，日照时数减少，月日照在230小时左右；秋季日照时数虽没有春季多，但比夏季要多，月日照230-245小时；冬季是一年中日照时数最少季节，月日照不足200小时，一般在170-190小时。</div>

