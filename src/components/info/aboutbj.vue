                    <h3>北京市政府出台文件推进立体绿化</h3>

                    <div class="gybj_cont">"十二五"时期，北京市将进一步加大对城市空间立体绿化的建设步伐，计划5年内完成100万平方米立体绿化任务。2011年7月15日，北京市政府《关于推进城市空间立体绿化建设工作的意见》对外发布，提出在城区特别是在中心城区建设"空中花园"，增加绿量。</div>

                    <div class="gybj_cont">北京市将通过强制性、鼓励性政策措施，发挥公共机构的示范带头作用，使城市空间立体绿化成为首都绿化美化的常态工作，不断提高城市空间立体绿化工程的建设标准、品质和社会认知程度，形成全社会共同参与的新格局。城市空间立体绿化是指建筑物或构筑物屋顶的绿化和建筑物或构筑物墙体、道路栏杆、立交桥、高架桥桥体等处垂直绿化，作为城市绿化的重要形式，在拓展城市绿色空间、美化生态景观、改善气候环境和生态服务功能等方面具有重要作用。自1984年长城饭店首次建设屋顶绿化以来，全市不断尝试采用新技术进行屋顶绿化，目前采取的主要方式是花园式绿化和草坪式绿化，已经积累了丰富经验。本次，北京市园林绿化局（首都绿化办）将负责制定城市空间立体绿化建设、养护行业标准，制定城市空间立体绿化建设计划和任务分解目标，协助完成工作任务，并做好涉及的政策说明、行业指导、监督实施、社会动员等工作。 </div>

                    <div class="gybj_cont">《意见》指出，公共机构所属建筑，在符合建筑规范、满足建筑安全要求的前提下，建筑层数少于12层、高度低于40米的非坡屋顶新建、改建建筑（含裙房）和竣工时间不超过20年、屋顶坡度小于15度的既有建筑，应当实施屋顶绿化。包括公共建筑、居住小区在内的各类新建、改建建筑附属绿化用地面积未达到规划要求，但项目用地范围内无地下设施的绿地面积已达到规划标准50%以上，建筑屋顶面积50%以上必须设计、建设花园式屋顶绿化，其屋顶绿化面积的20%可计入该项目附属绿化用地面积。北京市还制定了相应的鼓励措施，如屋顶绿化面积可计入区（县）绿化面积；新建、改建项目附属绿化用地面积在未计入屋顶绿化面积前已达到规划要求的，按屋顶绿化面积享受防洪费减免优惠政策等，以推动全社会共同参与。今年北京市将完成20万平方米立体绿化任务。目前，任务已被分解到全市各区（县）并按计划实施。</div>

