                    <div class="gybj_ml">历史沿革</div>

                    <div class="gybj_cont">中国西周时成为周朝的诸侯国之一的燕国的都城。自中国金朝起成为古代中国首都——中都。自元朝起，开始成为全中国的首都。元朝国都，元大都，或称大都，由于中国皇帝忽必烈是蒙古大汗国的大汗，蒙古文称为"汗八里"（Khanbaliq），意为"大汗之居处"。元大都其城址位于今北京市市区，北至元大都城遗址，南至长安街，东西至二环路。明朝自成祖后开始对北京进行大规模扩建，清朝在延续明北京城的基础上又进行了一些修缮和扩建。至清末北京成为当时世界上最大的城市。</div>

                    <div class="gybj_cont">北京有着3000余年的建城史和859余年的建都史。自秦汉以来北京地区一直是中国北方的军事和商业重镇，名称先后称为蓟城、燕都、燕京、涿郡、幽州、南京、中都、大都、京师、顺天府、北平、北京等。</div>

                    <h1>古代</h1>

                    <div class="gybj_cont">早在西周初年，周武王即封召公于北京及附近地区，称燕，都城在今北京房山区的琉璃河镇，遗址尚存。又封尧之后人（一说黄帝后人）于蓟，在今北京西南。后燕国灭蓟国，迁都于蓟，统称为燕都或燕京。</div>

                    <div class="gybj_cont">秦代设北京为蓟县，为广阳郡郡治。</div>

                    <div class="gybj_cont">汉高祖五年，本市被划入燕国辖地。元凤元年复为广阳郡蓟县，属幽州。本始元年因有帝亲分封于此，故更为广阳国首府。</div>

                    <div class="gybj_cont">东汉光武改制时，置幽州刺史部于蓟县。永元八年复为广阳郡驻所。</div>

                    <div class="gybj_cont">西晋时，朝廷改广阳郡为燕国，而幽州迁至范阳。十六国后赵时，幽州驻所迁回蓟县，燕国改设为燕郡，历经前燕、前秦、前燕、后燕和北魏的统治而不变。 </div>

                    <div class="gybj_cont">隋开皇三年废除燕郡。但很快在大业三年，隋朝改幽州为涿郡。</div>

                    <div class="gybj_cont">唐初武德年间，涿郡复称为幽州。贞观元年，幽州划归河北道管辖。后北京成为范阳节度使的驻地。安史之乱期间，安禄山在北京称帝，建国号为"大燕"。唐朝平乱后，复置幽州，归卢龙节度使节制。</div>

                    <div class="gybj_cont">五代初期，军阀刘仁恭在这里建立地方政权，称燕王，后被后唐消灭。北宋初年宋太宗在高梁河（今北京市海淀区）与辽战斗，北宋大败，对燕云十六州从此望眼欲穿；辽于会同元年（938年）起在北京地区建立了陪都（以此为起点计算北京建都史，至2009年，则为1071年），号南京幽都府，开泰元年改号析津府。</div>

                    <div class="gybj_cont">贞元元年（1153年），金朝皇帝海陵王完颜亮正式建都于北京（以此为起点计算北京建都史，至2010年，则为857年），称为中都，在今北京市西南。</div>

                    <div class="gybj_cont">元朝统一了全中国。蒙古大汗国改名中国元朝。自元朝起，开始成为全中国的首都[6][7][8]。元朝时的北京称为元大都。元大都成为全中国的交往中心，北到岭北行省，东到奴儿干都司（治所黑龙江下游），西到西藏地方，南到海南，都在此交流。此后明朝在大部分时间内和清朝的都城均建立于此，明朝从永乐年间，明成祖朱棣为了便于加强北方边防，保卫北方安全，将都城迁至北京。从那之后，北京成为了明朝的新都城。成吉思汗麾下大将木华黎于1215年攻下北京，遂设置燕京路大兴府。元世祖至元元年改称中都路大兴府。至元九年，中都大兴府正式改名为大都路（突厥语：Khanbalik，意为"汗城"，音译为汗八里、甘巴力克）。据马可波罗游记叙述：元人笃信佛，象为佛教祥兽，当时大都常有象只漫步。</div>

                    <div class="gybj_cont">明朝初年，以应天府（今南京）为京师，大都路于洪武元年八月改称为北平府，同年十月应军事需要划归山东行省。洪武二年三月，改为北平承宣布政使司驻地。燕王朱棣经靖难之变后夺得皇位后，于永乐元年改北平为北京，是为"行在"（天子行銮驻跸的所在，就称"行在"）且常驻于此，现在的北京也从此得名。北京城秩序井然，繁荣安乐。永乐十九年正月，明朝中央政府正式迁都北京，以顺天府北京为京师，应天府则作为留都称南京，明仁宗、英宗的部分时期，北京还曾一度降为行在，京师复为南京应天府。</div>

                    <div class="gybj_cont">清兵入关后即进驻北京，亦称北京为京师顺天府，属直隶省。</div>

                    <h1>近代</h1>

                    <h3>晚清时期（1860年－1912年）</h3>

                    <div class="gybj_cont">1860年北京条约签订以后，外国使节和传教士得到特许进北京，借传教的名义促使和参与了不平等条约的签定，搜集中国情报，进行文化侵略，传教士常常诉诸该国领事，以政治势力维护"传教"活动，在城内各处兴建教堂，使馆则集中在东交民巷。1860年，英法联军打进北京；1900年，八国联军再次打进北京，美丽的北京城受到了非常严重的破坏和洗劫，大量文物被侵略军和坏民劫掠。</div>

                    <h3>中华民国前期（1912年－1928年）</h3>

                    <div class="gybj_cont">1911年辛亥革命后，民国元年1月1日，中华民国定都南京，同年3月迁都北京，直至民国十七年中国国民党北伐军攻占北京，张作霖败回东北，北洋政府下台。</div>

                    <div class="gybj_cont">民国伊始，北京的地方体制仍依清制，称顺天府。直至民国三年，改顺天府为京兆地方，直辖于中央政府北洋政府。这一时期，北京新建了有轨电车系统，和一批现代的文化教育机构，如清华大学、燕京大学、北京大学、辅仁大学、协和医学院等。</div>

                    <h3>国军北伐至人民解放军进驻北平（1928年－1949年）</h3>

                    <div class="gybj_cont">1928年北伐战争后，由于蒋介石不重视北方边防，把首都迁到南京，撤销原京兆地方，北京改名为北平特别市。1930年6月，北平降格为河北省省辖市，同年12月复升为院辖市。这一时期，北京尽管不具首都的地位，但在教育方面仍有关键的优势，被国际人士称为"中国的波士顿"。1937年七·七事变后，北平被日本占领。伪中华民国临时政府在此成立，且将北平改名为北京。1945年8月21日，入侵北京的日本军队宣布投降，第十一战区孙连仲部接收北京，并重新更名为北平。</div>

                    <h3>中华人民共和国建立后（1949年－）</h3>

                    <div class="gybj_cont">1949年1月31日，傅作义将军与中国共产党达成和平协议，率领25万国民党军队投向正义，中国人民解放军进入北平市，实现对北京的解放。同年9月27日中国人民政治协商会议第一届全体会议通过《关于中华人民共和国国都、纪年、国歌、国旗的决议》，北平更名为北京。1949年10月1日，中华人民共和国中央人民政府在北京宣告成立。</div>

                    <div class="gybj_ml">地理环境</div>

                    <div class="gybj_cont_img">

                        <div>北京中心位于北纬39度54分20秒，东经116度25分29秒。位于华北平原西北边缘。毗邻渤海湾，上靠辽东半岛，下临山东半岛。北京与天津相邻，并与天津一起被河北省环绕。西部是太行山山脉余脉的西山，北部是燕山山脉的军都山，两山在南口关沟相交，形成一个向东南展开的半圆形大山弯，人们称之为"北京弯"，它所围绕的小平原即为北京小平原。诚如古人所言："幽州之地，左环沧海，右拥太行，北枕居庸，南襟河济，诚天府之国"。</div>

                       
