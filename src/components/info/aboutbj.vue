                    <h3>出租车</h3>

                    <div class="gybj_cont">2009年，出租车运营量6.7万辆。主要车型为北京现代伊兰特、捷达、爱丽舍，此外也有少量的红旗和富康轿车。夏利曾经是北京最为普通的出租车，但2006年已全部退出北京。2010年初亦新增加帕萨特和凯越两种车型。出租车白天起步价10元，2元/公里，15公里以后3元/公里。夜间（23点-5点）起步价11元，2.4元/公里，15公里以后3.4元/公里。2009年11月25日开始，北京市出租汽车加收燃油附加费，即乘客乘坐出租汽车超过3公里（基价公里）的，每次在计价器显示金额外加付1元。</div>

                    <div class="gybj_ml">文化艺术</div>

                    <div class="gybj_cont">北京是有着三千年历史的国家历史文化名城。北京在历史上曾为六朝都城，在从燕国起的2000多年里，建造了许多宏伟壮丽的宫廷建筑，使北京成为中国拥有帝王宫殿、园林、庙坛和陵墓数量最多，内容最丰富的城市。其中北京故宫，明朝时叫大内宫城，清朝时叫紫禁城，这里原为明、清两代的皇宫，住过24个皇帝，建筑宏伟壮观，完美地体现了中国传统的古典风格和东方格调，是中国乃至全世界现存最大的宫殿，是中华民族宝贵的文化遗产。天坛以其布局合理、构筑精妙而扬名中外，是明、清两代皇帝"祭天"。</div>

                    <h1>传统文化</h1><h3>四合院和胡同</h3>

                    <div class="gybj_cont">四合院是以正房、倒座房、东西厢房围绕中间庭院形成平面布局的北方传统住宅的统称。北京四合院源于元代院落式民居，是老北京城最主要的民居建筑。一座座青瓦灰砖的四合院之间形成的窄巷，就是著名的老北京胡同。</div>

                    <h3>庙宇</h3><div class="gybj_cont">北京的宗教寺庙遍布京城，现存著名的有：佛教的法源寺、潭柘寺、戒台寺、云居寺、八大处等。道教的白云观等。伊斯兰教的北京牛街礼拜寺等。藏传佛教（喇嘛教）的雍和宫等，天主教西什库天主堂、王府井天主堂等。基督教的缸瓦市教堂、崇文门教堂等。</div>

                    <h3>中轴线</h3><div class="gybj_cont">北京中轴线是指元、明、清时的北京城的中轴线，北京的城市规划具有以宫城为中心左右对称的特点。北京的中轴线南起永定门，北至钟鼓楼，长约7.8公里。从南往北依次为，永定门，前门箭楼，正阳门，中华门，天安门， 端门，午门，紫禁城，神武门，景山，地安门，后门桥，鼓楼和钟楼。从这条中轴线的南端永定门起，就有天坛、先农坛；太庙、社稷坛；东华门、西华门；安定门，德胜门以中轴线为轴对称分布。中国著名建筑大师梁思成先生曾经说："北京的独有的壮美秩序就由这条中轴线的建立而产生。" 永定门、中华门、地安门都在中华人民共和国成立后被拆毁，而近年来又重新修建了永定门城楼。</div>

                    <h3>城池</h3><div class="gybj_cont">北京城池是中国历史上最后两代王朝明和清的都城城防建筑的总称，由宫城、皇城、内城、外城组成，包括城墙、城门、瓮城、角楼、 敌台、护城河等多道设施，曾经是中国存世最完整的古代城市防御体系。北京城门是明清北京城各城门的总称。根据等级以及建筑规格的差异，分为宫城城门、皇城城门、内城城门、外城城门四类。明清北京城有宫城城门四座（一称六座） </div>

                    <h3>京剧</h3><div class="gybj_cont">京剧是地道的中国国粹。京剧的源头还要追溯到几种古老的地方戏剧，1790年，安徽的四大地方戏班——三庆班、四喜班、春公班、和春班——先后进京献艺，获得空前成功。徽班常与来自湖北的汉调艺人合作演出，于是，一种以徽调"二黄"和汉调"西皮"为主，兼收昆曲、秦腔、梆子等地方戏精华的新剧种诞生了，这就是京剧。在200年的发展历程中，京剧在唱词、念白及字韵上越来越北京化，使用的二胡、京胡等乐器，也融合了多个民族的特色，终于成为一种成熟的艺术。京剧集歌唱、舞蹈、武打、音乐、美术、文学于一体，与西方歌剧有类似之处，所以被西方人称为"Peking Opera"。</div>

                    <h3>艺术</h3><div class="gybj_cont">中国传统艺术家张师曾：原名：师曾，现居北京。书画大师范曾先生的大弟子张师曾，开以画入书法以书法入画的历史先河，中国古典传统人物画的领军人物。</div>

                    <h3>燕京八景</h3><div class="gybj_cont">燕京八景指北京旧时的八个景观，包括蓟门烟树（西土城）、卢沟晓月（卢沟桥）、金台夕照（金台路）、琼岛春荫（北海公园）、居庸叠翠（八达岭）、太液秋风（中南海）、玉泉趵突（玉泉山）和西山晴雪（香山、八大处）。北京地区八景的说法最早见金代古籍《明昌遗事》，此后历代方志包括《宛署杂记》（明）、《宸垣志略》（清）等对燕京八景均有提及，早期的燕京八景与之后的燕京八景略有出入，至清乾隆年间，乾隆皇帝亲自主持修订了燕京八景的说法并下旨建造御书燕京八景碑，燕京八景的景观和描述才固定下来。</div>

