                    <div class="gybj_cont">而公交卡每次乘坐的刷卡费用以0.4元起价（本地学生0.2元起），每个人只需要缴纳20元押金并储进一定金额就可以使用（第一次储值不低于20元，储值可以索要发票，押金没有）。如果您并不居住在北京，也可以买这样的公交卡，等到您要离开北京的时候，将卡退回指定的公交卡退卡点，就可以取回押金。</div>

