                    <div class="gybj_ml">交通运输</div>

                    <div class="gybj_cont">北京市城区的路网结构以矩形环状为主，道路多以此为依托，与经纬线平行网状分布。先后依托城市扩展，建设了二、三、四、五和六环路。总长度超过五百公里的北京新"七环路"已经形成半圆。截至2009年，全市公路里程20670公里，城市道路里程6206公里，轨道交通线路长度228公里。全市立交桥数共有381座，京哈、京沈、京津塘、京石、八达岭、京承、京开等多条高速公路流经北京。全市铁路总里程962公里，京秦铁路、京哈铁路、京沪铁路、京九铁路、京广铁路、京原铁路、京包铁路、京承铁路、京通铁路等多条铁路干线汇集于此。2009年底北京机动车保有量突破400万辆，2010年将逼近500万辆，城市交通面临更大考验。北京首都国际机场是亚洲第一大国际机场，目前已开通200多条国际国内航线，通往世界主要国家及地区和国内大部分城市，2009年吞吐旅客更超过6500万人次。北京已与世界上所有国家和地区通邮，国内直拨电话可达所有城市，国际直拨电话可达200多个国家和地区。</div>

                    <h1>铁路</h1>

                    <div class="gybj_cont">北京站：位于东便门西侧。建于1959年，是全国铁路客运重要枢纽，北京第三大火车站。</div>

                    <div class="gybj_cont">北京南站：位于永外大街。2008年改建完成，成为亚洲第二大的火车站，是京沪高速铁路的重要站点。（仅次于铁路上海虹桥站）</div>

                    <div class="gybj_cont">北京西站：位于莲花池东路。建于1996年，亚洲第一大火车站，是全球最大火车站之一。</div>

                    <div class="gybj_cont">北京北站：位于西直门。建于1905年，是京张铁路上的车站之一。现北京北站于2009年改建完成。</div>

                    <div class="gybj_cont">北京东站：位于百子湾。建于1938年。</div>

                    <div class="gybj_cont">北京是中国铁路网的中心之一，主要有北京到香港九龙的京九铁路，北京到上海的京沪铁路，北京到广州的京广铁路，北京到哈尔滨的京哈铁路，北京到包头的京包铁路，北京到原平的京原铁路，北京到通辽的京通铁路和北京到承德的京承铁路。大同到秦皇岛的大秦铁路也过境北京。在国际铁路运输方面，去往俄罗斯各城市、蒙古都城乌兰巴托和朝鲜都城平壤以及去往越南都城河内的列车均从北京发车。京津城际铁路于2005年7月4日开始修建，2008年8月1日正式开通。北京和天津两地的路程缩短为半小时。</div>

                    <div class="gybj_cont">中国最长的高速铁路即京港高速铁路将于2012年全线贯通。</div>

                    <h1>航空</h1>

                    <div class="gybj_cont">北京建有全国第一大国际机场和亚洲第一大国际机场—北京首都国际机场（Beijing Capital International Airport，PEK）。北京首都国际机场位于北京市顺义区（但行政上属于朝阳区）。</div>

                    <div class="gybj_cont">距北京市中心20公里。几乎所有北京的国内国际航班均在北京首都国际机场停靠和起飞。2009年北京首度国际机场完成旅客吞吐量6,533.73万人次，同比增长16.8%，亚洲排名第一，世界排名第三。北京首都国际机场是中国国际航空（Air China）的主要中心。机场和北京市区间由北京机场高速公路连接，在路况良好的情况下只需约40分钟即可到达。为迎接2008年北京奥运会，北京市还修建了一条北京地铁机场线（机场快轨），从市区东直门到达北京首都国际机场T2航站楼大约需要15分钟。</div>

                    <div class="gybj_cont">此外，北京还建有北京南苑机场（Beijing Nanyuan Airport，NAY）、北京良乡机场（Beijing Liangxiang Airport）、北京西郊机场（Beijing Xijiao Airport）、北京沙河机场（Beijing Shahe Airport）和北京八达岭机场（Beijing Badaling Airport）。这些机场除南苑机场同时为国内民用外（为中国联合航空公司的基地机场），其它很少向公众开放，主要为军用。</div>

                    <h1>市内交通</h1><h3>道路</h3>

                    <div class="gybj_cont">北京老城区（二环路以内）的城市道路是棋盘式的格局，横平竖直。东西方向的道路有长安街（复兴门至建国门）、平安大街（东四十条豁口至车公庄）、广安大街（广安门至广渠门）。南北方向的道路有中轴线，从玉蜓桥到雍和宫的东线和开阳桥到积水潭桥的西线。东线路过方庄、红桥、崇文门、东单、东四、北新桥和雍和宫；西线路过菜市口、宣武门、西单、西四、新街口。因为天安门广场和紫禁城的原因，北京的中轴线分为北中轴和南中轴。北中轴从地安门向北，经鼓楼、北辰路，达奥体中心；南中轴从前门向南经天桥、永定门，达三营门。南中轴和104国道重合。 </div>

                    <div class="gybj_cont">2009年，全市公路里程20670公里；其中，高速公路里程884公里，比上年末增加107公里。年末城市道路里程6206公里。</div>

                    <div class="gybj_cont">北京外围的城市道路则是环形加放射性的格局。连接京城还有十一条国道。</div>

